/**
 * Get the ruling entity for a country at a given year.
 * Finds the closest period that is <= the requested year.
 */
export function getRulingEntity(alpha3: string, year: number): string {
  const entries = rulingEntities[alpha3];
  if (!entries) return "Unknown";

  const years = Object.keys(entries)
    .map(Number)
    .sort((a, b) => a - b);

  let result = "Unknown";
  for (const y of years) {
    if (y <= year) result = entries[y];
    else break;
  }
  return result;
}

